'''
Numeric Data Types in Python
The numeric data type in Python represents the data that has a numeric value. A numeric value can be an integer, a floating point number,or even a complex number.
These values are defined as Python int, Python float, and Python complex classes in Python.
'''

# Integers
'''
This value is represented by int class. It contains positive or negative whole numbers(without fractions or decimals). 
In Python, there is no limit to how long an integer value can be.
'''
# Example
Number = 10
number = 100
negative_no = -100

# Float
'''
 This value is represented by the float class. It is a real number with a floating-point representation. It is specified by a decimal point. 
 Optionally, the character e or E followed by a positive or negative integer may be appended to specify scientific notation.
'''

# Example
Number = 10.0
number = 100.0
negative_no = -100.0

# Complex
'''
A complex number is represented by a complex class.A number in the form of a ± bj
where a is real part, bj is the imaginary part and the value of j is √ (- 1)(square root of minus 1)
'''

# Example
complex_number = 10 + 5.0j
